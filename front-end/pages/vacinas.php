        <h2>Importância das Vacinas</h2>
        <p>As vacinas são uma das maiores conquistas da medicina moderna, responsáveis por prevenir doenças graves e salvar milhões de vidas. Para os idosos, a vacinação é especialmente importante, pois o sistema imunológico enfraquece com a idade, tornando-os mais vulneráveis a infecções.</p>

        <h3>Mitos e Verdades sobre Vacinas</h3>
        
        <div class="myth-truth">
            <h4>Mito: As vacinas causam autismo.</h4>
            <p>Verdade: Diversos estudos científicos em larga escala concluíram que não há nenhuma ligação entre vacinas e autismo. Este mito começou devido a um estudo fraudulento que já foi descreditado.</p>
        </div>

        <div class="myth-truth">
            <h4 class="truth">Verdade: Vacinas ajudam a proteger comunidades inteiras.</h4>
            <p>Quando uma alta porcentagem da população está vacinada, a propagação de doenças é dificultada, protegendo até aqueles que não podem ser vacinados por motivos médicos.</p>
        </div>

        <div class="myth-truth">
            <h4>Mito: Vacinas não são necessárias porque muitas doenças foram erradicadas.</h4>
            <p>Verdade: Embora algumas doenças tenham se tornado raras em muitos lugares devido à vacinação, elas ainda existem em outras partes do mundo. Sem vacinação contínua, estas doenças podem ressurgir.</p>
        </div>

        <div class="myth-truth">
            <h4 class="truth">Verdade: Vacinas são seguras e passam por rigorosos testes de segurança.</h4>
            <p>Antes de serem aprovadas, as vacinas passam por extensos testes clínicos e são continuamente monitoradas para garantir sua segurança e eficácia.</p>
        </div>

        <h3>Importância das Vacinas para Idosos</h3>
        <p>Para os idosos, vacinas como a da gripe, pneumonia e herpes zoster são cruciais. Elas ajudam a prevenir complicações sérias e hospitalizações. A vacinação pode aumentar a qualidade de vida e a longevidade dos idosos, protegendo-os de doenças que podem ser fatais nesta faixa etária.</p>
